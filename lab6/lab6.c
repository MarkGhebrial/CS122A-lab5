// Set the pins controlled by the FPGA
void write_sprite(int rotation) {
    for (int y = 0; y < 16; y++){
        for (int x = 0; x < 16; x++) {
            switch (rotation) {
                case 0: spi_write16_blocking(spi0, &mem[y][x], 1); break;
                case 1: spi_write16_blocking(spi0, &mem[15-x][y], 1); break;
                case 2: spi_write16_blocking(spi0, &mem[15-y][15-x], 1); break;
                case 3: spi_write16_blocking(spi0, &mem[x][15-y], 1); break;
            }
        }
    }
}

int main() {
    // Initialize SPI 0 with baudrate 409600
    spi_init(spi0, 409600);
    spi_set_slave(spi0, false);
    spi_set_format(spi0, 16, SPI_CPOL_0, SPI_CPHA_0, SPI_MSB_FIRST);
    gpio_set_function(6, GPIO_FUNC_SPI); // SCK
    gpio_set_function(7, GPIO_FUNC_SPI); // TX


    busy_wait_ms(200);
    while (true) {
        for (int i = 0; i < 4; i ++) {
            write_sprite(i);
            busy_wait_ms(2000);

        }
    }
}
